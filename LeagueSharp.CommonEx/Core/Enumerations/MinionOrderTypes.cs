namespace LeagueSharp.CommonEx.Core.Enumerations
{
    /// <summary>
    ///     Enum that specifies how to order minions
    /// </summary>
    public enum MinionOrderTypes
    {
        /// <summary>
        ///     Don't order minion
        /// </summary>
        None,

        /// <summary>
        ///     Order by health
        /// </summary>
        Health,

        /// <summary>
        ///     Order by the maximum health
        /// </summary>
        MaxHealth
    }
}